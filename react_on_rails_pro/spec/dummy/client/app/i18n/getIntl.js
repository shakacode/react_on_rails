import { cache } from 'react';
import { createIntl } from 'react-intl/server';
// Import compiled translations from Rails YAML locale files
// Generated by ReactOnRails::Locales.compile via bin/shakapacker-precompile-hook
import { defaultLocale } from './generated/default';
import { translations } from './generated/translations';

/**
 * Get a cached intl instance for the given locale.
 * Uses React.cache() so all Server Components in the same request share one instance.
 */
const getIntl = cache((locale) => {
  const safeLocale = Object.hasOwn(translations, locale) ? locale : defaultLocale;
  return createIntl({
    locale: safeLocale,
    messages: translations[safeLocale],
  });
});

export default getIntl;
